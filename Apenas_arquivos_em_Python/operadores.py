# Operadores Relacionais
# Estes operadores são usados para comparações e tem grande utilidade para construir as lógicas de programação
# Os operadores são: == != > < >= <=

# Exemplos individuais

x = 10 # Atribuindo 10 a variável 'x'
y = 10 # Atribuindo 10 a variável 'y'

# Agora testaremos os operadores com True ou False nos print's

print (x == y) # True
print (x != y) # False

x = 5  # Atribuindo 5 a variável 'x' que é a mesma variável anterior, apenas mudei o valor armazenado nela
y = 10 # Atribuindo 10 a variável 'y' que é a mesma variável anterior, apenas mudei o valor armazenado nela

print (x == y) # False
print (x != y) # True
print (x >= y) # False
print (x <= y) # True