'''Para aceitar acentos'''

print (2 + 2) # Mostra o resultado do somatório de dois mais dois
soma = 2 + 2
print (soma) # Outra forma de fazer

print (2 - 2) # Mostra o resultado da subtração de dois menos dois
subtracao = 2 - 2
print (subtracao) # Outra forma de fazer

print (10 / 5) # Mostra o resultado da divisao de dez por 5
divisao = 10 / 5
print (divisao) # Outra forma de fazer

print (10 % 3) # Mostra o resultado do resto da divisao inteira de 10 por 3, tem resto 1
resto_da_divisao_inteira = 10 % 3
print (resto_da_divisao_inteira) # Outra forma de fazer

print (10 ** 2) # Mostra o resultado de 10 ao quadrado
expoente = 10 ** 2
print (expoente) # Outra forma de fazer